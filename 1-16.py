# Algorithmic complexity: O(n ** 4)

from timeit import default_timer as timer

# start benchmark
start = timer()

n = 1770
max_n = int(n ** (1 / 2))
my_sums = []

for i in range(0, max_n + 1):
    i_squared = i ** 2

    for j in range(i, max_n + 1):
        j_squared = j ** 2

        for k in range(j, max_n + 1):
            k_squared = k ** 2

            for l in range(k, max_n + 1):
                l_squared = l ** 2
                my_sum = i_squared + j_squared + k_squared + l_squared

                if my_sum == n:
                    my_sums.append([i, j, k, l])

print('my_sums:', my_sums)
print('len my_sums:', len(my_sums))

# stop benchmark
end = timer()
duration = end - start
print('duration:', duration)

